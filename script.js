function filterImages() {
    const type = document.getElementById('filter-type').value;
    const hero = document.getElementById('filter-hero').value;
    const duty = document.getElementById('filter-duty').value;

    const grid = document.getElementById('image-grid');
    grid.innerHTML = '';

    for (const item of data) {
        if (type && item.type !== type) continue;
        if (hero && item.name !== hero) continue;
        if (duty && item.duty !== duty) continue;

        const div = document.createElement('div');
        div.className = 'image-item';

        const img = document.createElement('img');
        img.src = './png/' + item.code + ".png";
        img.alt = item.name;
        img.title = item.name;
        img.dataset.name = item.name;
        img.onclick = () => appendText("<TXC00" + item.code + ">");

        div.appendChild(img);
        grid.appendChild(div);
    }
}


function initFilters() {
    const typeSet = new Set();
    const dutySet = new Set();
    const heroSet = new Set();

    for (const item of data) {
        if (item.type) typeSet.add(item.type);
        if (item.duty) dutySet.add(item.duty);
        if (item.type === '英雄') heroSet.add(item.name);
    }

    // 填充类型
    const typeSelect = document.getElementById('filter-type');
    typeSelect.innerHTML = '<option value="">全部类型</option>' +
        [...typeSet].map(t => `<option value="${t}">${t}</option>`).join('');

    // 填充职责
    const dutySelect = document.getElementById('filter-duty');
    dutySelect.innerHTML = '<option value="">全部职责</option>' +
        [...dutySet].map(d => `<option value="${d}">${d}</option>`).join('');

    // 填充英雄（只列出 hero 类型）
    const heroSelect = document.getElementById('filter-hero');
    heroSelect.innerHTML = '<option value="">全部英雄</option>' +
        [...heroSet].map(h => `<option value="${h}">${h}</option>`).join('');
}


function appendText(text) {
    const textarea = document.getElementById('output');
    const start = textarea.selectionStart;
    const end = textarea.selectionEnd;
    const before = textarea.value.substring(0, start);
    const after = textarea.value.substring(end);

    textarea.value = before + text + after;

    // 将光标移到插入文本后
    const newPos = start + text.length;
    textarea.selectionStart = textarea.selectionEnd = newPos;

    // 保持 textarea 聚焦
    textarea.focus();
}

function colorChanged() {
    const colorInput = document.getElementById('color');
    const color = colorInput.value;
    document.getElementById('color-preview').style.backgroundColor = color;

    const hex = color.slice(1).toUpperCase() + "FF";
    const formatted = `<FG${hex}>`;
    appendText(formatted);
}

function copyToClipboard() {
    const textarea = document.getElementById('output');
    textarea.select();
    document.execCommand('copy');
}

